package net.solarnetwork.central.in.tracker.domain;

import net.solarnetwork.central.domain.BaseStringEntity;

/**
 * A global metric campaign entity.
 * 
 * @author matt
 * @version 1.0
 */
public class GlobalMetricCampaign extends BaseStringEntity {

	private static final long serialVersionUID = 3117436486012792329L;

	private String name;
	private String description;
	private boolean enabled;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public boolean isEnabled() {
		return enabled;
	}

	public void setEnabled(boolean enabled) {
		this.enabled = enabled;
	}

}
